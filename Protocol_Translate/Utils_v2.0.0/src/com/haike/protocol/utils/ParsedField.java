package com.haike.protocol.utils;


public class ParsedField {
	
	private String name;
	private String desc;
	private String data_type;
	private String data_length;
	private String unit;
	private String unit_f;
	private String max;
	private String min;
	private String value;
	private String value_desc;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getData_type() {
		return data_type;
	}
	public void setData_type(String data_type) {
		this.data_type = data_type;
	}
	public String getData_length() {
		return data_length;
	}
	public void setData_length(String data_length) {
		this.data_length = data_length;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getUnit_f() {
		return unit_f;
	}
	public void setUnit_f(String unit_f) {
		this.unit_f = unit_f;
	}
	public String getMax() {
		return max;
	}
	public void setMax(String max) {
		this.max = max;
	}
	public String getMin() {
		return min;
	}
	public void setMin(String min) {
		this.min = min;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getValue_desc() {
		return value_desc;
	}
	public void setValue_desc(String value_desc) {
		this.value_desc = value_desc;
	}
	
	
	
		
}
